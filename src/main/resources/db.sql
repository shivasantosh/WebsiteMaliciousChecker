CREATE TABLE `NE_ORDER_ITEMS_NEW` (
  `ORDER_ITEM_ID` int(11) NOT NULL AUTO_INCREMENT,
  `ORDER_ID` bigint(20) DEFAULT NULL,
  `SKU_CODE` varchar(200) CHARACTER SET latin1 DEFAULT NULL,
  `PRODUCT_NAME` varchar(200) CHARACTER SET latin1 DEFAULT NULL,
  `CATEGORY` varchar(200) CHARACTER SET latin1 DEFAULT NULL,
  `PRICE` bigint(20) DEFAULT NULL,
  `QUANTITY` bigint(20) DEFAULT NULL,
  `TOTAL` bigint(20) DEFAULT NULL,
  `MARGIN` bigint(20) DEFAULT NULL,
  `CLIENT_ORDER_ID` varchar(200) CHARACTER SET latin1 DEFAULT NULL,
  PRIMARY KEY (`ORDER_ITEM_ID`),
  KEY `ORDER_ID` (`ORDER_ID`),
  KEY `ORDER_ITEM_ID` (`ORDER_ITEM_ID`),
  CONSTRAINT `__fK_KEY` FOREIGN KEY (`ORDER_ID`) REFERENCES `NE_ORDER_NEW` (`ORDER_ID`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=10 DEFAULT CHARSET=utf8








 